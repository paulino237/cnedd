						<li>Construction d’un centre de recherche et de gestion des énergies renouvelables dans la région de l’Ouest ;</li>
	
						<li>Création d’un référentiel sur les caractéristiques méthanogènes des déchets organiques au Cameroun ;</li>
	
						<li>Création d’un journal traitant des sujets de transition énergétique. </li>
