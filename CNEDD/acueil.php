		<div id="content2">
			<div id="texte">
				<p id="partie1">
					<span id="titre">Qui sommes-nous ?</span><br><br>
					Nous sommes une association, acteur de la transition énergétique, conformément aux Objectifs de Développement Durable (ODD) définis par l’Organisation des nations-Unies, suivi des recommandations de la Conférence des Parties (COP21) tenue à Paris en 2016. <br>
					De résumé, nous travaillons et sensibilisons pour la prise en compte par notre état de la transition énergétique dans la définition des politiques de développement, et contribuons dans le même ordre d’idée aux développement des outils et techniques concourant à la réduction du taux d’émission de carbone, et par ricochet à l’éradication de Gaz à Effet de Serre (GES).
					
				</p>
			</div>
			
			<div id="texte">
				<p id="partie2">
					<span id="titre">Nos objectifs</span></br><br>
					Acteur de la lutte contre le réchauffement climatique et ses conséquences néfastes sur notre environnement et notre santé (perte de fertilité des sols, assèchement des cours d’eau et de la faune aquatique, prolifération des maladies infectieuses…), la prise en compte de la transition permettront de : 
					<ul>
						<li>Donner une seconde vie à nos déchets utilisés pour produire des énergies renouvelables et non-polluantes (électricité, BioGNL, BioGPL, GNC) ; </li>
	
						<li>Développer l’agriculture biologique, car permettant de produire un excellent fertilisant et non-polluant ; </li>
	
						<li>Créer une indépendance aux énergies fossiles polluantes, et dont nous ne pouvons maitrisons ni les coûts, ni la disponibilité, car dépendant de la situation socio-économiques des pays dits développés ;	</li>
	
						<li>Promouvoir une mobilité durable (biocarburants) ;	</li>
	
						<li>Promouvoir l’accès aux infrastructures de traitement des eaux usées et à l’eau potable ;</li>
	
						<li>Promouvoir l’immobilier écologique c’est-à-dire construire des logements n’émettant pas de carbone.</li>
					</ul>
					
				</p>
			</div>
			
			<div id="texte">
				<p id="partie3">
					<span id="titre">Nos valeurs</span>
					<ul>
						<li>Lutter contre l’émission de carbone et par ricochet la réduction de Gaz à Effet de Serre (GES),		</li>
	
						<li>Garantir l’accès des populations aux énergies vertes et à faibles coûts ;</li>
	
						<li>Limiter l’utilisation des énergies fossiles polluantes ;</li>
	
						<li>Promouvoir les énergies renouvelables dites vertes ;</li>
	
						<li>Améliorer la qualité de vie des populations.</li>
					</ul>
					
				</p>
				
			</div>
			
			
			<div id="texte">
				<p id="partie4">
					<span id="titre">Nos missions</span>
					<ul>
						<li>Production d’énergies renouvelables ;</li>
						<li> Production d’engrais biologiques ;</li>
						<li> Production de gaz renouvelables ;</li>
						<li>Traitement des eaux (usées et potable) ;</li>
						<li> Production de biocarburants.</li>
					</ul>
					
				</p>
	
			</div>

			
			<div id="texte">
				<p id="partie5">
					<span id="titre">Nos projets</span>
